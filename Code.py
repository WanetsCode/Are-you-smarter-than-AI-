import random

PLAYER_EMOJI = "ðŸ˜¶"
END_EMOJI = "ðŸ§«"
WALL = "ðŸŸ¨"
PATH = "ðŸ’ "
rend = -1

def generate_maze(width, height):
    maze = [[WALL if random.random() < 0.3 else PATH for _ in range(width)] for _ in range(height)]
    maze[0][0] = PATH  # Start point
    maze[rend][rend] = END_EMOJI  # End point
    return maze

def print_maze(maze, player_pos):
    for y, row in enumerate(maze):
        for x, tile in enumerate(row):
            if (x, y) == player_pos:
                print(PLAYER_EMOJI, end="")
            else:
                print(tile, end="")
        print()

def move_player(player_pos, direction, maze):
    x, y = player_pos
    if direction == "W" and y > 0 and maze[y-1][x] != WALL:
        y -= 1
    elif direction == "S" and y < len(maze) - 1 and maze[y+1][x] != WALL:
        y += 1
    elif direction == "A" and x > 0 and maze[y][x-1] != WALL:
        x -= 1
    elif direction == "D" and x < len(maze[0]) - 1 and maze[y][x+1] != WALL:
        x += 1
    return x, y

def is_solvable(maze, start, end):
    width, height = len(maze[0]), len(maze)
    stack = [start]
    visited = set()
    visited.add(start)

    while stack:
        x, y = stack.pop()
        if (x, y) == end:
            return True
        for dx, dy in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
            nx, ny = x + dx, y + dy
            if 0 <= nx < width and 0 <= ny < height and (nx, ny) not in visited and maze[ny][nx] != WALL:
                stack.append((nx, ny))
                visited.add((nx, ny))
    return False

